"""DOCX renderer for Report 2 — Environment & Industry Overlay.

Reuses the shared `docx_helpers` scaffolding so Report 2 visually matches
Report 1 (Arial 11pt body, 14pt H2, bordered tables, 1" margins). Only
the content differs.

Keep this file a thin formatter — every structured value comes from the
`generate()` dict produced by `EnvironmentReport`.
"""
from __future__ import annotations

from pathlib import Path
from typing import Any

from reports.docx_helpers import (
    new_document,
    add_heading,
    add_paragraph,
    add_table,
    add_bullet,
    set_footer,
)


def render_docx(data: dict[str, Any], path: Path | str) -> Path:
    """Render Report 2 to a Word document and return the written path."""
    meta = data["meta"]
    title = meta["report_title"]
    subtitle = _subtitle(meta)
    doc = new_document(title, subtitle=subtitle)

    _render_executive_summary(doc, data)
    _render_industry_risk(doc, data)
    _render_property_market(doc, data)
    _render_downturn_scenarios(doc, data)
    _render_methodology(doc, data)

    set_footer(doc, "Generated by External Benchmark Engine — Report 2")
    path = Path(path)
    doc.save(str(path))
    return path


# ---------------------------------------------------------------------------
# Section renderers
# ---------------------------------------------------------------------------


def _subtitle(meta: dict[str, Any]) -> str:
    bits = [meta["report_subtitle"], f"Generated {meta['generated_at'][:10]}"]
    if meta.get("data_as_of"):
        bits.append(f"Data as-of {meta['data_as_of']}")
    stale = [f["name"] for f in meta.get("freshness_findings", []) if f["is_stale"]]
    if stale:
        bits.append(f"STALE EXPORTS: {', '.join(stale)}")
    return " · ".join(bits)


def _render_executive_summary(doc, data: dict[str, Any]) -> None:
    add_heading(doc, "1. Executive Summary", level=2)
    exec_ = data["executive_summary"]
    add_paragraph(doc, exec_["regime_interpretation"])
    for line in exec_["bullets"]:
        add_bullet(doc, _strip_md(line))
    # Flagship callout box
    add_paragraph(
        doc,
        f"Flagship read-through: macro regime is '{exec_['regime_flag']}'; "
        f"{exec_['elevated_industry_count']} of {exec_['industry_count']} industries "
        f"sit at 'Elevated' base risk.",
        bold=True,
    )


def _render_industry_risk(doc, data: dict[str, Any]) -> None:
    add_heading(doc, "2. Industry Risk Outlook", level=2)
    iro = data["industry_risk_outlook"]
    add_paragraph(
        doc,
        f"All {len(iro['rows'])} industries ranked by structural base risk score. "
        f"Macro conditioner for this cycle: cash rate {iro['cash_rate_latest']:.2f}% "
        f"({iro['cash_rate_change_1y']:+.2f} pp over 1 year). "
        "The macro component is applied uniformly to every industry by design — "
        "it is a single environmental conditioner, not an industry-specific signal.",
    )
    add_table(
        doc,
        headers=["Rank", "Industry", "Classification", "Macro", "Base", "Level"],
        rows=[
            [r["rank"], r["industry"],
             f"{r['classification_risk']:.2f}",
             f"{r['macro_risk']:.2f}",
             f"{r['base_risk']:.2f}",
             r["level"]]
            for r in iro["rows"]
        ],
        widths_inches=[0.6, 2.6, 1.1, 0.9, 0.7, 0.9],
    )
    add_heading(doc, "Top-3 drivers", level=3)
    for bullet in iro["top3_commentary"]:
        add_bullet(doc, _strip_md(bullet))
    add_heading(doc, "Construction caveat", level=3)
    add_paragraph(doc, _strip_md(iro["construction_caveat"]), italic=True)


def _render_property_market(doc, data: dict[str, Any]) -> None:
    add_heading(doc, "3. Property Market Outlook", level=2)
    pmo = data["property_market_outlook"]
    add_paragraph(
        doc,
        f"{pmo['total_segments']} property segments grouped by cycle stage "
        "(Downturn → Slowing → Neutral → Growth). Within each stage, segments "
        "are ordered by market-softness score (highest first).",
    )
    for group in pmo["groups"]:
        add_heading(doc, f"{group['stage_display']} ({len(group['rows'])} segments)", level=3)
        add_paragraph(doc, group["narrative"], italic=True)
        add_table(
            doc,
            headers=["Segment", "Softness", "Band", "Region risk",
                     "Region band", "Approvals Δ%"],
            rows=[
                [r["property_segment"],
                 f"{r['softness_score']:.2f}",
                 r["softness_band"],
                 f"{r['region_risk_score']:.2f}",
                 r["region_risk_band"],
                 f"{r['approvals_change_pct']:+.1f}"]
                for r in group["rows"]
            ],
            widths_inches=[2.8, 0.8, 0.9, 0.8, 0.9, 0.8],
        )
    add_heading(doc, "Concentration and tailwinds", level=3)
    for bullet in pmo["commentary"]:
        add_bullet(doc, _strip_md(bullet))


def _render_downturn_scenarios(doc, data: dict[str, Any]) -> None:
    add_heading(doc, "4. Downturn Scenario Overlays", level=2)
    dwn = data["downturn_scenarios"]
    add_paragraph(
        doc,
        "PD / LGD / CCF multipliers and property-value haircuts for each "
        "downturn scenario. Applied downstream by the benchmark engine as the "
        "stressed overlay on top of adjusted benchmark values.",
    )
    add_table(
        doc,
        headers=["Scenario", "PD ×", "LGD ×", "CCF ×", "Property haircut", "As-of"],
        rows=[
            [r["scenario_display"],
             f"{r['pd_multiplier']:.2f}",
             f"{r['lgd_multiplier']:.2f}",
             f"{r['ccf_multiplier']:.2f}",
             f"{r['property_value_haircut']:.2f}",
             r["as_of_date"]]
            for r in dwn["rows"]
        ],
        widths_inches=[1.2, 0.9, 0.9, 0.9, 1.3, 1.1],
    )
    add_heading(doc, "Interpretation", level=3)
    for r in dwn["rows"]:
        add_bullet(
            doc,
            f"{r['scenario_display']} — {r['interpretation']}",
        )
    # Monotonicity audit flag
    mono_ok = all([dwn["monotonic_pd"], dwn["monotonic_lgd"],
                   dwn["monotonic_ccf"], dwn["monotonic_haircut"]])
    if mono_ok:
        add_paragraph(
            doc,
            "Monotonicity check: PASS. Multipliers increase monotonically base → severe "
            "across all four columns.",
            italic=True,
        )
    else:
        add_paragraph(
            doc,
            "Monotonicity check: FAIL. One or more multiplier columns is not "
            "monotonically increasing base → severe. Review upstream contract "
            "before adopting these overlays.",
            bold=True,
        )


def _render_methodology(doc, data: dict[str, Any]) -> None:
    add_heading(doc, "5. Methodology Notes", level=2)
    mn = data["methodology_notes"]
    add_paragraph(doc, mn["intro"])
    add_heading(doc, "Structural vs current-state", level=3)
    add_paragraph(doc, mn["structural_vs_current_state"])
    add_heading(doc, "Construction review item", level=3)
    add_paragraph(doc, _strip_md(mn["construction_review"]))
    add_heading(doc, "Options considered", level=3)
    add_paragraph(doc, mn["options_considered"])
    add_heading(doc, "Path taken", level=3)
    add_paragraph(doc, _strip_md(mn["path_taken"]))
    add_heading(doc, "Property-signal proxy caveat", level=3)
    add_paragraph(doc, mn["property_proxy_caveat"])
    add_paragraph(doc, f"Upstream source: {mn['upstream_project']}", italic=True)


def _strip_md(text: str) -> str:
    """DOCX paragraphs don't render markdown bold/italic; strip the markers."""
    return text.replace("**", "")
