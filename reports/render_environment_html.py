def _html_table(headers: list[str], rows: Iterable[list[Any]]) -> str:
    out = ["<table><thead><tr>"]
    for h in headers:
        out.append(f"<th>{_e(h)}</th>")
    out.append("</tr></thead><tbody>")
    for row in rows:
        out.append("<tr>")
        for cell in row:
            out.append(f"<td>{_e(cell) if not _looks_prefmt(cell) else cell}</td>")
        out.append("</tr>")
    out.append("</tbody></table>")
    return "".join(out)


def _e(s: Any) -> str:
    return _html.escape(str(s)) if s is not None else ""


def _looks_prefmt(cell: Any) -> bool:
    """Cells already escape-safe (our float-formatted numerics). Always re-escape for safety."""
    return False


def _md_to_html(text: str) -> str:
    """Minimal markdown → HTML: just handles **bold**. No links, no code spans."""
    escaped = _e(text)
    out = []
    in_bold = False
    i = 0
    while i < len(escaped):
        if escaped[i:i + 2] == "**":
            out.append("</strong>" if in_bold else "<strong>")
            in_bold = not in_bold
            i += 2
        else:
            out.append(escaped[i])
            i += 1
    if in_bold:
        out.append("</strong>")
    return "".join(out)
