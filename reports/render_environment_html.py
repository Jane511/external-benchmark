def render_html(data: dict[str, Any], path: Path | str) -> Path:
    meta = data["meta"]
    title = meta["report_title"]
    parts: list[str] = [
        "<!DOCTYPE html>",
        "<html><head>",
        f"<title>{_e(title)}</title>",
        "<meta charset='utf-8'>",
        _STYLES,
        "</head><body>",
        f"<h1>{_e(title)}</h1>",
        f"<p class='subtitle'>{_subtitle_html(meta)}</p>",
    ]

    # Table of contents
    parts.append("<div class='toc'><strong>Contents</strong><ul>")
    for key, label in SECTION_ORDER:
        anchor = key.replace("_", "-")
        parts.append(f"<li><a href='#{anchor}'>{_e(label)}</a></li>")
    parts.append("</ul></div>")

    parts.append(_render_executive_summary_html(data))
    parts.append(_render_industry_risk_html(data))
    parts.append(_render_property_market_html(data))
    parts.append(_render_downturn_html(data))
    parts.append(_render_methodology_html(data))

    parts.append("<p class='subtitle'>Generated by External Benchmark Engine — Report 2</p>")
    parts.append("</body></html>")

    rendered = "\n".join(parts)
    path = Path(path)
    path.write_text(rendered, encoding="utf-8")
    return path


